
public class Chap6Ex3 {
	public static void main(String[] args) {
		int[][] a  = {
				{1, 2, 3},
				{4, 5}
		};

		System.out.print("2次元配列aの1次元の要素数は");
		System.out.println(a.length);

		System.out.print("2次元配列aの1番目の配列の要素数は ");
		System.out.println(a[0].length);

		System.out.print("2次元配列aの2番目の配列の要素数は ");
		System.out.println(a[1].length);

	}

}
